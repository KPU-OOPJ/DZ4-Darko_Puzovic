
public class konvertor {
    public double konvertujKmUMi (double kilometri){
        return kilometri/1.67;
    }
    public double konvertujMiuKm (double milje){
        return milje*1.67;
    }
    public double konvertujMUSt (double metri){
        return metri/0.32;
    }
    public double konvertujStUM (double stope){
        return stope*0.32;
    }
    public double konvertujKgULb (double kilogrami){
        return kilogrami/0.45;
    }
    public double konvertujLbUKg (double libre){
        return libre*0.45;
    }
}
